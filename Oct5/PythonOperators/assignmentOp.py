n1 = 10
n2 = 20
n1 = n2
print(n1, n2) #now n1=20

n1+=n2 #n1=n1+n2 --20+20=40, now n1=40
print(n1)

n1-=n2 #n1=n1-n2 --40-20=20, now n1=20
print(n1)

n1*=n2 #n1=n1*n2 --20*20=400, now n1=400
print(n1)

n1/=n2 #n1=n1/n2 --400/20=20, now n1=20
print(n1)

n1%=n2 #n1=n1%n2 --20%20=0, now n1=0
print(n1)

n1**=n2
print(n1) #n1=n1**n2 --20%20=0, now n1=0




